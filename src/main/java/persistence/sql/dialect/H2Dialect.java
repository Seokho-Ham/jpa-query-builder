package persistence.sql.dialect;

import jakarta.persistence.GenerationType;
import persistence.sql.constant.ColumnType;

public class H2Dialect extends Dialect {

    @Override
    public String getTypeName(ColumnType columnType) {
        String typeName;
        switch (columnType) {
            case VARCHAR:
                typeName = "VARCHAR(255)";
                break;
            case INTEGER:
                typeName = "INTEGER";
                break;
            case BIGINT:
                typeName = "BIGINT";
                break;
            case BOOLEAN:
                typeName = "BOOLEAN";
                break;
            default:
                throw new UnsupportedColumnTypeException();
        }

        return typeName;
    }

    @Override
    public String getGenerationStrategy(GenerationType generationType) {
        String generationStrategy;
        switch (generationType) {
            case AUTO:
                generationStrategy = "generated by default as identity";
                break;
            case IDENTITY:
                generationStrategy = "generated by default as identity";
                break;
            case UUID:
                generationStrategy = "";
                break;
            case TABLE:
                generationStrategy = "";
                break;
            case SEQUENCE:
                generationStrategy = "";
                break;
            default:
                throw new UnsupportedGenerationTypeException();
        }

        return generationStrategy;
    }


}
